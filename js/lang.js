// --- Language Logic ---

// Get current language or default to 'vi'
function getLang() {
    return localStorage.getItem('hub_lang') || 'vi';
}

// Set language and reload
function setLang(lang) {
    if (lang !== 'vi' && lang !== 'en') return;
    localStorage.setItem('hub_lang', lang);
    location.reload(); // Reload to refresh all charts and content safely
}

// Update static content on page load
function updateStaticContent() {
    const lang = getLang();
    const t = translations[lang];

    // Update Text Elements with data-i18n
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (t[key]) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = t[key];
            } else {
                // Check if key has HTML (like <br>) or simple text
                // Using innerHTML is flexible but be careful with user input (here it is static config)
                el.innerHTML = t[key];
            }
        }
    });

    // Update HTML lang attribute
    document.documentElement.lang = lang;

    // Update Switcher Button State 
    const btnVi = document.getElementById('lang-btn-vi');
    const btnEn = document.getElementById('lang-btn-en');

    if (btnVi && btnEn) {
        if (lang === 'vi') {
            btnVi.classList.add('active', 'fw-bold');
            btnEn.classList.remove('active', 'fw-bold');
            btnEn.style.opacity = '0.6';
            btnVi.style.opacity = '1';
        } else {
            btnEn.classList.add('active', 'fw-bold');
            btnVi.classList.remove('active', 'fw-bold');
            btnVi.style.opacity = '0.6';
            btnEn.style.opacity = '1';
        }
    }
}

// Run on load
document.addEventListener('DOMContentLoaded', updateStaticContent);

